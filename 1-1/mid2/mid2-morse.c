#include <stdio.h>
#include <stdlib.h>
#include <string.h>

int N, pos = 0, letter = 0, count = 0;
char prev, string[1000], read[20] = {'\0'}, save[1000] = {'\0'};
char encode[26][20] = {{'=','.','=','=','=','\0'},                                  // A
                       {'=','=','=','.','=','.','=','.','=','\0'},                  // B
                       {'=','=','=','.','=','.','=','=','=','.','=','\0'},          // C
                       {'=','=','=','.','=','.','=','\0'},                          // D
                       {'=','\0'},                                                  // E
                       {'=','.','=','.','=','=','=','.','=','\0'},                  // F
                       {'=','=','=','.','=','=','=','.','=','\0'},                  // G
                       {'=','.','=','.','=','.','=','\0'},                          // H
                       {'=','.','=','\0'},                                          // I
                       {'=','.','=','=','=','.','=','=','=','.','=','=','=','\0'},  // J
                       {'=','=','=','.','=','.','=','=','=','\0'},                  // K
                       {'=','.','=','=','=','.','=','.','=','\0'},                  // L
                       {'=','=','=','.','=','=','=','\0'},                          // M
                       {'=','=','=','.','=','\0'},                                  // N
                       {'=','=','=','.','=','=','=','.','=','=','=','\0'},          // O
                       {'=','.','=','=','=','.','=','=','=','.','=','\0'},          // P
                       {'=','=','=','.','=','=','=','.','=','.','=','=','=','\0'},  // Q
                       {'=','.','=','=','=','.','=','\0'},                          // R
                       {'=','.','=','.','=','\0'},                                  // S
                       {'=','=','=','\0'},                                          // T
                       {'=','.','=','.','=','=','=','\0'},                          // U
                       {'=','.','=','.','=','.','=','=','=','\0'},                  // V
                       {'=','.','=','=','=','.','=','=','=','\0'},                  // W
                       {'=','=','=','.','=','.','=','.','=','=','=','\0'},          // X
                       {'=','=','=','.','=','.','=','=','=','.','=','=','=','\0'},  // Y
                       {'=','=','=','.','=','=','=','.','=','.','=','\0'}};         // Z

void save_letter(){
    if(read[pos-1] == '.') read[pos-1] = '\0';
    for(int j=0;j<26;j++){                         // 去比對目前read裡面的字串跟encode裡面的哪個一樣
        if(strcmp(read, encode[j]) == 0){          // 把對應到的字母存進去
            save[letter++] = 'A' + j;
            save[letter] = '\0';
        }
    }
    memset(read, 0, sizeof(read));                // 初始化一下
    pos = 0;
    count = 0;
}

int main(){
    scanf("%d %s", &N, string);
    for(int i=0;i<N;i++){
        if(i) prev = string[i-1];
        if(count == 3 && string[i] != '.'){                     // 數到三個'.'代表已經讀了一個字母               
            save_letter();
            i--;                                                // i要往前退一格，不然會少讀一個
        }
        else if(count == 7){                                    // count == 7同理，只是要加空格
            save_letter();
            i--;
            save[letter++] = ' ';
            save[letter] = '\0';
        }
        else if(string[i] == '='){                              // 遇到'='就放進read，並保持最後一格是'\0'
            read[pos++] = string[i];
            read[pos] = '\0';
        }
        else if(string[i] == '.'){                              // 遇到'.'要判斷，不是第一個的話就count++
            if(prev == '.'){                                    // 是的話就放進read
                count++;
            }
            else{
                read[pos++] = string[i];
                read[pos] = '\0';
                count = 1;
            }
        }
    }
    save_letter();
    puts(save);
}